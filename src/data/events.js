// Bugünün tarihine göre olayı bul
export function bugununOlayi() {
  const bugun = new Date();
  bugun.setHours(0, 0, 0, 0);
  
  // Bugünün tarihine en yakın olayı bul
  const olay = tarihiOlaylar.find(e => {
    const olayTarihi = new Date(e.tarih);
    olayTarihi.setHours(0, 0, 0, 0);
    return olayTarihi.getTime() === bugun.getTime();
  });
  
  return olay || tarihiOlaylar[0]; // Bulunamazsa ilk olayı döndür
}

// Tarihe göre olay bul
export function tariheGoreOlay(tarih) {
  const hedefTarih = new Date(tarih);
  hedefTarih.setHours(0, 0, 0, 0);
  
  const olay = tarihiOlaylar.find(e => {
    const olayTarihi = new Date(e.tarih);
    olayTarihi.setHours(0, 0, 0, 0);
    return olayTarihi.getTime() === hedefTarih.getTime();
  });
  
  return olay || null;
}

// Son 30 günü getir
export function son30Gun() {
  const gunler = [];
  const bugun = new Date();
  
  for (let i = 0; i < 30; i++) {
    const tarih = new Date(bugun);
    tarih.setDate(bugun.getDate() - i);
    gunler.push(tarih);
  }
  
  return gunler;
}

